def row_for(k):
    # en/tr/ko from STRINGS, ja/id/vi/th/ru/es/pt/fr/ar appended from the parallel lists.
    return STRINGS[k] + (JA[k], ID[k], VI[k], TH[k], RU[k], ES[k], PT[k], FR[k], AR[k])


def main():
    master = MASTER.read_text(encoding='utf-8')
    errors = []
    for i, lang in enumerate(LANGS, start=1):
        out = master
        for k in range(len(STRINGS)):
            row = row_for(k)
            src, dst = row[0], row[i]
            n = out.count(src)
            if n != 1:
                errors.append(f'[{lang}] "{src[:60]}..." matched {n} times')
                continue
            out = out.replace(src, dst)
        path = MASTER.with_name(f'sinoma_promo_{lang}.html')
        path.write_text(out, encoding='utf-8', newline='\n')
        print(f'wrote {path.name} ({len(out)} bytes)')
    if errors:
        print('\n'.join(errors))
        sys.exit(1)


if __name__ == '__main__':
    main()
